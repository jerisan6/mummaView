function cambiarImagenSilver(hover) {
    const logoImage = document.getElementById('silver-image');
    if (hover) {
        logoImage.src = 'recursos/Home/Confian/Silver/Silver_2.svg';
    } else {
        logoImage.src = 'recursos/Home/Confian/Silver/Silver_1.svg';
    }
}
